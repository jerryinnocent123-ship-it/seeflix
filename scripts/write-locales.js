const writeJson = (locale, object) => {
  const localeDir = path.join('src', 'locales', locale);
  fs.mkdirSync(localeDir, { recursive: true });
  const targetPath = path.join(localeDir, 'common.json');
  fs.writeFileSync(targetPath, JSON.stringify(object, null, 2), 'utf8');
};

Object.entries(data).forEach(([locale, object]) => writeJson(locale, object));
